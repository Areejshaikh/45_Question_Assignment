console.log("*".repeat(50));
var userName = "Areej";
console.log(userName == "Areej"); // true
console.log(userName === "areej"); // false
var age = 54;
console.log(age >= 50); // true
console.log(age <= 30); // false
console.log(age === 54); // true
console.log(age === 45); // false
console.log(age > 60); // true
console.log(age < 60); // false
console.log(userName === "Areeg" && age === 54); // false
console.log(userName === "Areeg" || age === 54); // true
var number = [12, 13, 14, 15, 16];
console.log(number.length === 67); // false
console.log(number.length === 13); // true
